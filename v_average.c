#include "t_v3.h"

t_v3	v_average(const t_v3 *a, const t_v3 *b)
{
	return ((t_v3)
		{
			(a->x + b->x) / 2,
			(a->y + b->y) / 2,
			(a->z + b->z) / 2,
		});
}
