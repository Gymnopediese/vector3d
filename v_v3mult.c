#include "t_v3.h"

t_v3	v_v3mult(const t_v3 *a, const t_v3 *b)
{
	return ((t_v3){
		a->y * b->z - a->z * b->y,
		a->z * b->x - a->x * b->z,
		a->x * b->y - a->y * b->x,
	});
}
